public class Main {
    // Assignment Operators

    public static void main(String[] args) {
        // Assignment Operators

        int x = 100;
        int y = 20;

        // Re-assign an existing variables
        x = x + y;
        x += y; // This does the same as above

        x -= y;
        x *= y;
        x /= y;
        x %= y;

        x++; // Increments the integer value by 1
        x--; // Decrements the integer value by 1
    }
}